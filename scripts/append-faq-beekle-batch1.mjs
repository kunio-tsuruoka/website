console.log(`Targets: ${Object.keys(ENHANCEMENTS).length} columns`);
console.log('---');

let succeeded = 0;
const errors = [];

for (const [slug, enhancement] of Object.entries(ENHANCEMENTS)) {
  console.log(`[SLUG] ${slug}`);

  let current;
  try {
    current = await client.get({
      endpoint: 'columns',
      contentId: slug,
      queries: { fields: 'content' },
    });
  } catch (e) {
    console.error(`   NG: 取得失敗 ${e.message}`);
    errors.push(`${slug}: fetch failed`);
    continue;
  }

  const stripped = stripOldCta(current.content);
  const newContent = `${stripped}\n${enhancement.beekleSection}\n${buildFaqHtml(enhancement.faqs)}\n{{CONTACT_CTA}}\n`;

  console.log(`   bytes before: ${current.content.length}`);
  console.log(
    `   bytes after : ${newContent.length}  (delta +${newContent.length - current.content.length})`
  );
  console.log(`   FAQ items   : ${enhancement.faqs.length}`);

  if (dryRun) {
    console.log('   OK: would PATCH (dry-run)');
    succeeded++;
    continue;
  }

  try {
    await client.update({
      endpoint: 'columns',
      contentId: slug,
      content: { content: newContent },
    });
    console.log('   OK: PATCH applied');
    succeeded++;
  } catch (e) {
    console.error(`   NG: ${e.message}`);
    errors.push(`${slug}: ${e.message}`);
  }
}

console.log('\n========================================');
console.log(`${dryRun ? 'plan' : 'patched'}: ${succeeded}/${Object.keys(ENHANCEMENTS).length}`);
if (errors.length > 0) {
  console.log(`errors: ${errors.length}`);
  for (const e of errors) console.log(`   - ${e}`);
  process.exit(1);
}
if (dryRun) {
  console.log('\nThis was a dry run. Re-run with --apply to actually PATCH.');
}
