/**
 * 旧 CTA を取り除く: <h3 ...>Beekleに...</h3> から最後までを丸ごと削る。
 * 既存記事はすべて末尾が `<h3>Beekleにご相談ください</h3><p>...</p><p><a>...</a></p>` 構造。
 */
function stripOldCta(content) {
  return content.replace(/<h3[^>]*>Beekleに[\s\S]*$/, '').trim();
}

function buildFaqHtml(faqs) {
  let html = '\n<h2>よくある質問</h2>\n';
  for (let i = 0; i < faqs.length; i++) {
    html += `\n<h2>Q${i + 1}. ${faqs[i].q}</h2>\n<p>A. ${faqs[i].a}</p>\n`;
  }
  return html;
}

